package operator;

public class Penugasan {
	public static void main(String[] args) {
		int x = 25;
		System.out.println(x);
		x += 3; // x = x + 3
		System.out.println(x);
		x -= 3; // x = x - 3
		System.out.println(x);
		x *= 3; // x = x * 3
		System.out.println(x);
		x /= 3; // x = x / 3
		System.out.println(x);
		x %= 3; // x = x % 3
		System.out.println(x);
		}
}
